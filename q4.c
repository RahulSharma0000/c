// Write a  program to find the Area of Triangle given Base and Height
// [Area = 0.5 * Base * Height ]

// Sample Input → Height : 10

// Sample Input → Base : 15

// Sample Output → Area of the triangle is 75

#include <stdio.h>
int main()
{
    int base = 15;
    int height = 10;
    int area = 0.5 * base * height;

    printf("Area of Triangle is = %i", area);

    return 0;
}